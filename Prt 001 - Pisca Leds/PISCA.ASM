ORG 00H
AJMP INICIO
ORG 03H
AJMP EXT0
ORG 0BH
AJMP TIM_0


INICIO:	MOV SP,#2FH

 	MOV IE,#83H
        MOV IP,#02H
	
        MOV TMOD,#01H
	MOV TH0,#03CH	
	MOV TL0,#0B0H 						
	SETB TR0
	CLR IT0

MAIN:
		
	AJMP	MAIN


EXT0:			
	CLR  P2.0
	RETI

TIM_0:	
	CPL P2.0	
	MOV TH0,#03CH	
	MOV TL0,#0B0H 
	RETI
END