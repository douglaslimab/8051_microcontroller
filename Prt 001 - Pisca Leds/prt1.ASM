		ORG		000h
		AJMP		INICIO
		ORG		003h
		AJMP		INT_0
		ORG		00Bh
		AJMP		TIM_0
		ORG		013h
		AJMP		INT_1
		ORG		01Bh
		AJMP		TIM_1
;-------------------------------------------------------------------------------------------
LINHA		EQU		P2
;-------------------------------------------------------------------------------------------
INICIO:		MOV		SP,#03Fh
		MOV		A,#0FEh
MAIN:		RR		A
		MOV		SAIDA,A
		ACALL		DELAY
		AJMP		MAIN
		
;-------------------------------------------------------------------------------------------
INT_0:		
		RETI
;-------------------------------------------------------------------------------------------
INT_1:
		RETI
;-------------------------------------------------------------------------------------------
TIM_0:
		RETI
;-------------------------------------------------------------------------------------------
TIM_1:
		RETI
;-------------------------------------------------------------------------------------------
DELAY:		MOV		R0,#0FFh
VOLTA:		MOV		R1,#0FFh
		DJNZ		R1,$
		DJNZ		R0,VOLTA
		RET
;-------------------------------------------------------------------------------------------
		END